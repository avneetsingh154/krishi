document.querySelector(".buy1").addEventListener("click", function () {
  const msp = 1200;
  prompt("Minimum Price per Quintal is", msp);
  let qnt = prompt("enter Quantity");
  let prc = prompt("Enter Price per 100kg");
  let rat = Number(prc);

  let feedback =
    rat * qnt >= msp * qnt
      ? (window.location.href = "/rfarmer.html")
      : "Sorry you cannot buy under MSP";

  alert(feedback);
});
